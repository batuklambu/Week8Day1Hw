// // What is Z-index?
// Z Index ( z-index ) is a CSS property that defines the order of overlapping HTML elements. Elements with a higher index will be placed on
// top of elements with a lower index. Note: Z index only works on positioned elements
// ( position:absolute , position:relative , or position:fixed ).

// // What is a Vendor Prefix and What is it's Purpose?

// CSS Vendor prefixes (or browser prefixes) are a way for browsers to give access to new CSS features not yet considered stable. By using prefixes,
// we can use these newer CSS features with the browsers that support them — instead of waiting for all browsers to catch up.

// // What is the difference between CSS Grid and Flexbox?
// Grid and flexbox. The basic difference between CSS Grid Layout and CSS Flexbox Layout is that flexbox was designed for layout in one
// dimension - either a row or a column. Grid was designed for two-dimensional layout - rows, and columns at the same time.

